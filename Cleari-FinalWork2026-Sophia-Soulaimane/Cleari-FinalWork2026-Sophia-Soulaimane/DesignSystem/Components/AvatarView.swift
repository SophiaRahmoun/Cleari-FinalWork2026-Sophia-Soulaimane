import SwiftUI

struct AvatarView: View {
    let imageName: String
    var size: CGFloat = 110

    var body: some View {
        Image(imageName)
            .resizable()
            .scaledToFill()
            .frame(width: size, height: size)
            .clipShape(Circle())
    }
}
